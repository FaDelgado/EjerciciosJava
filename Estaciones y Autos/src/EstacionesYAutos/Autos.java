package EstacionesYAutos;

public class Autos {

		private String marca;
		private String color;
		private int tanque;
		private int plata;
		public Autos(String marca, String color, int tanque, int plata) {
			super();
			this.marca = marca;
			this.color = color;
			this.tanque = tanque;
			this.plata = plata;
		}
		public String getMarca() {
			return marca;
		}
		public void setMarca(String marca) {
			this.marca = marca;
		}
		public String getColor() {
			return color;
		}
		public void setColor(String color) {
			this.color = color;
		}
		public int getTanque() {
			return tanque;
		}
		public void setTanque(int tanque) {
			this.tanque = tanque;
		}
		public int getPlata() {
			return plata;
		}
		public void setPlata(int plata) {
			this.plata = plata;
		}
		
		
		
		
		
		
}
